package com.tai.game.scripts.operatorClass;

public class OperatorInfo {
	
	public String id;
	public String name;
	
	public OperatorInfo(String id, String name) {
		this.id = id;
		this.name = name;
	}
	
	public String getId() {
		return this.id;
	}
	
	public String getName() {
		return this.name;
	}
	
}
